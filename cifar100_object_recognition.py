X_train_hog = []
X_test_hog = []

for image in trnImage:
    features = hog(
        image,
        pixels_per_cell=(8, 8),
        cells_per_block=(2, 2),
        channel_axis=-1
    )
    X_train_hog.append(features)

for image in tstImage:
    features = hog(
        image,
        pixels_per_cell=(8, 8),
        cells_per_block=(2, 2),
        channel_axis=-1
    )
    X_test_hog.append(features)

X_train_hog = np.array(X_train_hog)
X_test_hog = np.array(X_test_hog)

"""HOG features were extracted from all training and testing images to prepare the dataset for traditional machine learning models."""

print("Training HOG shape:", X_train_hog.shape)
print("Testing HOG shape:", X_test_hog.shape)

from sklearn.decomposition import PCA

"""PCA was imported to reduce the number of features while keeping the most important image information."""

pca = PCA(n_components=100)

X_train_pca = pca.fit_transform(X_train_hog)
X_test_pca = pca.transform(X_test_hog)

print("Original HOG shape:", X_train_hog.shape)
print("Reduced PCA shape:", X_train_pca.shape)

from sklearn.svm import SVC
from sklearn.metrics import accuracy_score

"""The SVM classifier and evaluation metric were imported for image classification and performance measurement."""

svm_model = SVC(kernel='rbf')

svm_model.fit(X_train_pca, trnLabel_fine)

"""The SVM model was trained using the PCA-reduced HOG features and the fine category labels."""

svm_predictions = svm_model.predict(X_test_pca)

"""The trained SVM model was used to predict the categories of the testing images."""

svm_accuracy = accuracy_score(tstLabel_fine, svm_predictions)

print("SVM Accuracy:", svm_accuracy)

svm_model_coarse = SVC(kernel='rbf')

svm_model_coarse.fit(X_train_pca, trnLabel_coarse)

svm_coarse_predictions = svm_model_coarse.predict(X_test_pca)

svm_coarse_accuracy = accuracy_score(tstLabel_coarse, svm_coarse_predictions)

print("SVM Coarse Accuracy:", svm_coarse_accuracy)

