from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Conv2D, MaxPooling2D, Flatten, Dense

"""TensorFlow and Keras layers were imported to build the Convolutional Neural Network (CNN) model.

"""

trnImage_cnn = trnImage.astype('float32') / 255.0
tstImage_cnn = tstImage.astype('float32') / 255.0
#The image pixel values were normalized to improve CNN training performance.

cnn_model = Sequential([
    Conv2D(32, (3,3), activation='relu', input_shape=(32,32,3)),
    MaxPooling2D((2,2)),

    Conv2D(64, (3,3), activation='relu'),
    MaxPooling2D((2,2)),

    Flatten(),

    Dense(128, activation='relu'),
    Dense(100, activation='softmax')
])
#A CNN architecture was created for image classification using convolution, pooling, and dense layers.

cnn_model.compile(
    optimizer='adam',
    loss='sparse_categorical_crossentropy',
    metrics=['accuracy']
)

history = cnn_model.fit(
    trnImage_cnn,
    trnLabel_fine,
    epochs=10,
    batch_size=64,
    validation_data=(tstImage_cnn, tstLabel_fine)
)

